# set list, initialize a loop
magicians = ['alice', 'david', 'carolina']
for x in magicians:
    print(x)

# range function for series of numbers
for value in range(1,5):
    print(value)

# store for loop into list
squares = []
for value in range(1,11):
    square = value**2
    squares.append(square)
print(squares)

# define a tuple, using round brackets
dimensions = (200, 50)
print(dimensions[1])

